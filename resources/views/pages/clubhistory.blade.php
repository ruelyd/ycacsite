                <h5 style="padding-bottom: 10px ;margin-top: 40px">ORIGIN OF YOKOHAMA’S YC&AC</h5> 
                <p style="text-align: justify;">
                    he history of the YC&AC has always been recognized as starting one day in 1868 in
                    the dining room of the Yokohama home of 23-year-old Scotsman JP (James Pender) Mollison, 
                    who was mad about cricket and who had only recently become a local resident. 
                    On that day, he and his cricketing friend Ernest Price and a few others founded the Yokohama 
                    Cricket Club (YCC) with Mollison as the president and Price as secretary.
                </p>   
